SELECT 

[SEHID], 
SEH_SEHREG.[PATIENTNR], 
[AANKSDATUM], 
[AANKSTIJD],
[AANGEMAAKT], 
[AANMTIJD], 
[INDICATIE],
[TRANSTEXT], 
[BESTEMMING] 

FROM RONTGEN_RONTVERR

INNER JOIN RONTGEN_RONTVRVR ON RONTGEN_RONTVERR.RONTVERRID = RONTGEN_RONTVRVR.RONTVERRID 
INNER JOIN RONTGEN_RONTVRSL ON RONTGEN_RONTVRVR.VERSLAGNR = RONTGEN_RONTVRSL.RONTVSLID  
INNER JOIN WI_DOCUMENT ON RONTGEN_RONTVRSL.DOCID = WI_DOCUMENT.ID 
INNER JOIN RONTGEN_RONTGEN ON RONTGEN_RONTVRSL.ONDERZNR = RONTGEN_RONTGEN.ONDERZNR
INNER JOIN SEH_SEHREG ON SEH_SEHREG.PATIENTNR = RONTGEN_RONTGEN.PATIENTNR

WHERE [AANKSDATUM] > '20150101' 
AND RONTGEN_RONTGEN.FUNCTIEAFD = 'FUNC000001'
AND DATEDIFF(day, AANKSDATUM, AANGEMAAKT) <= 1
AND DATEDIFF(day, AANKSDATUM, AANGEMAAKT) >= 1

ORDER BY AANKSDATUM desc
